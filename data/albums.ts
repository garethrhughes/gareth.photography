function photos(slug: string, count: number, label: string) {
  return Array.from({ length: count }, (_, i) => {
    const filename = `${String(i + 1).padStart(3, "0")}.jpg`;
    const dim = dimensions[slug]?.[filename];
    return {
      src: photoUrl(slug, filename),
      alt: label,
      ...(dim ?? {}),
    };
  });
}

